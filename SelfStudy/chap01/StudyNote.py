name = 'seina'
# 名前を表示
print(name)

# 改行しての表示
print \
(name)

# 1 行に収めて表示
name = 'seina'; print (name)

# 計算結果を出力
print (2*5)

# カンマ区切りで複数の変数を作成
print (name, 'yamada')

# sep=区切り文字
print (name, 'yamada', sep='@')

# 改行を除く
print (name, 'yamada', end=' ')
print ('welcome')

print (name, 'yamada', sep='@', end=' ')
print ('welcome')

# 複数行のコメント
print(name)
